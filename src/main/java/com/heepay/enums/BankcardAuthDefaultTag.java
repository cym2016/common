package com.heepay.enums;

public enum BankcardAuthDefaultTag {
	 /**
     * 未知
     */
    NONDEFAULT(0,"否"),
    DEFAULT(1,"是");


    int _value;

    /**
     * 存放内容
     */
    String _Content;

    /**
     * 私有构造函数
     * @param value 枚举值
     * @param content 缓存内容
     * @return
     */
    BankcardAuthDefaultTag(int value, String content) {
        this._value = value;
        this._Content = content;
    }

    /**
     * 取得枚举对象值
     * @return 枚举对象值
     */
    public int getValue() {
        return this._value;
    }

    /**
     * 取得内容
     * @return 内容
     */
    public String getContent() {
        return this._Content;
    }
}
